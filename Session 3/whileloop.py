#WHILE Loop runs until condition is false
#In the end of each iteration, you must change the value of variable which is used in condition, otherwise your loop will become an infinite loop
#In some IDEs, A forever loop might give you a runtime error
#In some cases, a forever loop might even crash your computer

i = -1
while i < 21:
    print(i)
    i += 1

